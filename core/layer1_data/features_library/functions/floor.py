# ==============================================================================
# § 公式 | 向下取整 (Floor)
# ==============================================================================
import polars as pl


def calculate(series: pl.Expr, **kwargs) -> pl.Expr:
    """
    對序列進行向下取整。

    契約：
    - series: pl.Expr, 一個 Polars 表達式。

    返回：
    一個 Polars 表達式，代表向下取整後的序列。
    """
    return series.floor()
